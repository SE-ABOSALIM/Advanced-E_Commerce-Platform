  static String _currentLanguage = _defaultLanguage;
  static List<Function()> _listeners = [];

  static String get currentLanguage => _currentLanguage;
  
  // Dil değişikliklerini dinlemek için listener ekleme
  static void addListener(Function() listener) {
    _listeners.add(listener);
  }
  
  // Listener'ı kaldırma
  static void removeListener(Function() listener) {
    _listeners.remove(listener);
  }

  static Future<void> initialize() async {
    final prefs = await SharedPreferences.getInstance();
    String? savedLanguage = prefs.getString(_languageKey);
    
    // Eğer kaydedilmiş dil yoksa, telefon dilini algıla
    if (savedLanguage == null) {
      savedLanguage = _detectDeviceLanguage();
    }
    
    _currentLanguage = savedLanguage;
  }

  // Telefon dilini algılama fonksiyonu
  static String _detectDeviceLanguage() {
    final deviceLocale = ui.window.locale;
    final languageCode = deviceLocale.languageCode.toLowerCase();
    
    // Desteklenen dilleri kontrol et
    if (_supportedLanguages.containsKey(languageCode)) {
      return _supportedLanguages[languageCode]!;
    }
    
    // Türkçe ve Arapça dışındaki diller için İngilizce varsayılan
    return 'English';
  }

  static Future<void> setLanguage(String language) async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_languageKey, language);
    _currentLanguage = language;
    
    // Tüm listener'ları bilgilendir
    for (var listener in _listeners) {
      listener();
    }
  }

  static String translate(String key) {
    final translations = _translations[_currentLanguage];
    return translations?[key] ?? key;
  }

  static List<String> get availableLanguages => ['Türkçe', 'English', 'العربية'];
  
  // Dil kodunu döndüren fonksiyon
  static String getLanguageCode(String languageName) {
    for (var entry in _supportedLanguages.entries) {
      if (entry.value == languageName) {
        return entry.key;
      }
    }
    return 'English'; // Varsayılan olarak İngilizce
  }
  
  // Mevcut dil kodunu döndüren fonksiyon
  static String get currentLanguageCode => getLanguageCode(_currentLanguage);
